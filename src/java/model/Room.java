package model;

/**
 *
 * @author ADMIN
 */
public class Room {
    private int id;
    private int doctorId;  
    private String name;

    public Room() {
    }

    public Room(int id, int doctorId, String name) {
        this.id = id;
        this.doctorId = doctorId;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getDoctorId() {
        return doctorId;
    }

    public void setDoctorId(int doctorId) {
        this.doctorId = doctorId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
